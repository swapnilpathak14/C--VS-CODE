#include <iostream>
using namespace std;
int main()
{
    //define a function of integers, characters, floats
    int m,n;
    cout<<"Enter the value of two integers:";
    cin>> m >> n;
    cout<<"m= "<<m<<" n= "<<n;
    float x,y,z;
    cout<<"\nEnter three decimal numbers:";
    cin>> x >> y >> z;
    cout<<"x = "<<x<<" y ="<<y<<" z = "<<z;
    char c1,c2,c3;
    cout<<"\nEnter three characters:";
    cin>> c1 >> c2 >> c3;
    cout<<"c1 = "<<c1<<" c2 = "<<c2<<" c3 = "<<c3;
    return 0;
}